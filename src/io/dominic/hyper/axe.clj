(defmacro deftag
  [tag]
  `(defmacro ~tag
     [& args#]
     (let [tag# ~(name tag)]
       `(h ~tag# ~@args#))))

(comment
  (deftag foo)
  (macroexpand-1 '(deftag foo))
  (macroexpand-1 '(foo {:hi 1} (foo))))

